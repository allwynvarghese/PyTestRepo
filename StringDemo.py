str = "Hello world world world"

print(str[1])  # getting the character of a string by index

print(str[0:7])  # substring

print(str.split(" "))  # split string operation. splits string into a list

print(str.lower())  # to lower character

print(str.upper())  # to upper character

print(str.capitalize())  # capitalize string

print("Count of 'world' in string is ",str.count("world"))  # counts the occurrence of the substring in the string

print( "Hello" in str)  # return boolean value based on the presence of substring in a string

str1 = " whats app "

print(str1.strip())  # trims leading and ending spaces

print(str1.lstrip())  # trims only left spaces

print(str1.rstrip())  # trims only right spaces

str3 = "Hello"

print("Is str alphabetic? ",str3.isalpha())  # checks if string is alphabetic






